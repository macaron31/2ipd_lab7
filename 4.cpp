#include <iostream>

using namespace std;


int main()
{
	setlocale(0, "");

	int num = 0;

	cout << "Ведите число: ";
	cin >> num;

	int symma = 0;
	int proizvedenie = 1;

	while (true)
	{
		int lastNum = num % 10;
		symma += lastNum;
		proizvedenie *= lastNum;
		num /= 10;

		if (num == 0)
			break;
	}

	cout << "Сумма: " << symma << endl;
	cout << "Произведение: " << proizvedenie << endl;

}

