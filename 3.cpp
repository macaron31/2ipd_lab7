#include <iostream>

using namespace std;


int main()
{
	setlocale(0, "");

	int num = 0;

	cout << "Ведите число: ";
	cin >> num;

	for (int i = 1; i <= 10; i++)
	{
		if (num % i == 0)
		{
			cout << "Делится на: " << i << endl;
		}
	}


}

